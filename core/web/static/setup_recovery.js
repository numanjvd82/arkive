(function() {
  const checkbox = document.getElementById("confirm-backup");
  const submit = document.querySelector("[data-recovery-submit]");
  const downloadButtons = document.querySelectorAll("[data-recovery-download]");
  const printButtons = document.querySelectorAll("[data-recovery-print]");
  const words = Array.from(document.querySelectorAll(".recovery-word"));

  function pdfEscape(text) {
    return String(text)
      .replace(/\\/g, "\\\\")
      .replace(/\(/g, "\\(")
      .replace(/\)/g, "\\)");
  }

  function buildRecoveryPDF() {
    const lines = [
      "Arkive Core - Vault Recovery Key",
      "",
      "This is the ONLY way to recover your data if you lose your password.",
      "Store this recovery key securely. Losing it will result in permanent data loss.",
      ""
    ];

    const phraseWords = [];

    words.forEach(function(wordNode) {
      const value = wordNode.querySelector(".recovery-word-value");
      if (!value) {
        return;
      }
      phraseWords.push(value.textContent.trim());
    });

    for (let i = 0; i < phraseWords.length; i += 4) {
      lines.push(phraseWords.slice(i, i + 4).join("    "));
    }

    lines.push("");
    lines.push("Generated by Arkive Core setup.");

    const contentParts = ["BT", "/F1 20 Tf", "72 760 Td", "(" + pdfEscape(lines[0]) + ") Tj", "ET"];
    let currentY = 730;

    for (let i = 1; i < lines.length; i++) {
      const line = lines[i];
      const fontSize = i < 5 ? 11 : 14;
      const font = i < 5 ? "F1" : "F2";
      contentParts.push("BT");
      contentParts.push("/" + font + " " + fontSize + " Tf");
      contentParts.push("72 " + currentY + " Td");
      contentParts.push("(" + pdfEscape(line) + ") Tj");
      contentParts.push("ET");
      currentY -= line === "" ? 12 : 22;
    }
    const content = contentParts.join("\n");
    const objects = [];

    function addObject(body) {
      objects.push(body);
      return objects.length;
    }

    const catalogId = addObject("<< /Type /Catalog /Pages 2 0 R >>");
    const pagesId = addObject("<< /Type /Pages /Kids [3 0 R] /Count 1 >>");
    const pageId = addObject("<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Resources << /Font << /F1 4 0 R /F2 5 0 R >> >> /Contents 6 0 R >>");
    const font1Id = addObject("<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>");
    const font2Id = addObject("<< /Type /Font /Subtype /Type1 /BaseFont /Courier-Bold >>");
    const contentId = addObject("<< /Length " + content.length + " >>\nstream\n" + content + "\nendstream");

    const ordered = [catalogId, pagesId, pageId, font1Id, font2Id, contentId];
    let pdf = "%PDF-1.4\n";
    const offsets = [0];

    ordered.forEach(function(id) {
      offsets[id] = pdf.length;
      pdf += id + " 0 obj\n" + objects[id - 1] + "\nendobj\n";
    });

    const xrefOffset = pdf.length;
    pdf += "xref\n0 " + (objects.length + 1) + "\n";
    pdf += "0000000000 65535 f \n";

    for (let i = 1; i <= objects.length; i++) {
      pdf += String(offsets[i]).padStart(10, "0") + " 00000 n \n";
    }

    pdf += "trailer\n<< /Size " + (objects.length + 1) + " /Root 1 0 R >>\n";
    pdf += "startxref\n" + xrefOffset + "\n%%EOF";

    return new Blob([pdf], { type: "application/pdf" });
  }

  function downloadRecoveryPDF() {
    const blob = buildRecoveryPDF();
    const url = URL.createObjectURL(blob);
    const anchor = document.createElement("a");
    anchor.href = url;
    anchor.download = "arkive-vault-recovery-key.pdf";
    document.body.appendChild(anchor);
    anchor.click();
    document.body.removeChild(anchor);
    setTimeout(function() {
      URL.revokeObjectURL(url);
    }, 1000);
  }

  function syncSubmitState() {
    if (!checkbox || !submit) {
      return;
    }
    submit.disabled = !checkbox.checked;
  }

  if (checkbox) {
    checkbox.addEventListener("change", syncSubmitState);
    syncSubmitState();
  }

  downloadButtons.forEach(function(button) {
    button.addEventListener("click", function() {
      downloadRecoveryPDF();
    });
  });

  printButtons.forEach(function(button) {
    button.addEventListener("click", function() {
      window.print();
    });
  });
})();
